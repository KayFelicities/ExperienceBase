  <blockquote>
    <p><span>四、共享是渐进共享</span></p><span></span></blockquote>
  <blockquote>
    <p><span></span></p>
    <p><span>　　“合抱之木,生于毫末；九层之台,起于累土；千里之行,始于足下。”渐进共享是对共享发展的进程而言的。习近平指出：“一口吃不成胖子，共享发展必将有一个从低级到高级、从不均衡到均衡的过程，即使达到很高的水平也会有差别。我们要立足国情、立足经济社会发展水平来思考设计共享政策，既不裹足不前、铢施两较、该花的钱也不花，也不好高骛远、寅吃卯粮、口惠而实不至。”</span></p>
    <p><span>　　坚持渐进共享，要做到既积极而为又量力而行。正确处理当前和长远的关系，既尽力解决当前必须解决和能够解决的民生问题，又充分考虑各方面的条件和可承受能力，滴水穿石，久久为功，积小胜为大胜，不断提升人民群众的获得感。共同富裕是社会主义的本质要求。一方面，我们要清醒地认识到，实现这个目标需要一个漫长的历史过程。我国正处于并将长期处于社会主义初级阶段，我们不能做超越阶段的事情；另一方面，也不在逐步实现共同富裕方面无所作为，而是要根据现有条件把能做的事情尽量做起来，积小胜为大胜，不断朝着全体人民共同富裕的目标前进。</span></p>
    <p><span>　　一个人要想过上富裕的生活就必须靠自己长期奋斗来实现，国家也同样如此。虽然我国的经济持续保持高速增长，在各方面都取得了长足进步，但我们也要意识到，这样的成绩来之不易，不是一蹴而就的，是靠一点一滴积累而来的。我们要从长远角度理性看待今天存在的各种差别问题，立足国情，立足当前经济社会发展水平来思考问题，不要夸夸其谈，不急躁，不冲动，不许假大空式“口惠而实不至”的承诺。习近平指出：“我们要随时随刻倾听人民呼声、回应人民期待，保证人民平等参与、平等发展权利，维护社会公平正义，在学有所教、老有所得、病有所医、老有所养、住有所居上持续取得新进展，不断实现好、维护好、发展好最广大人民根本利益，使发展成果更多更公平惠及全体人民，在经济社会不断发展的基础上，朝着共同富裕方向稳步前进。”</span></p>
  </blockquote>
  <p>
    <br>
