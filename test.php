  <blockquote>
    <p><span>一、共享是全民共享<span></span></span><span> &nbsp; &nbsp; &nbsp; &nbsp; &nbsp;</span></p>
  </blockquote>
  <blockquote>
    <p><span>　　“安得万里裘，盖裹周四垠；稳暖皆如我，天下无穷人。”全民共享是讲发展成果要覆盖全民。习近平指出：“共享发展是人人享有、各得其所，不是少数人共享、一部分人共享。”</span></p>
    <p><span>　　改革开放38年来，我国经济社会发展取得了巨大成就，人民的总体生活得到了极大改善。但毋庸讳言，我国财富的分配格局尚存在不合理之处，反映在城乡之间、不同地域、不同行业之间的收入差距仍然较大。国家统计局发布数据显示，2014年，我国农村居民人均可支配收入远低于城镇居民。按照现行扶贫标准，2014年年底全国还有14个集中连片特殊困难地区，592个国家扶贫开发工作重点县，12.8万个贫困村，2948.5万个贫困户，7017万贫困人口，贫困人口超过500万的还有贵州、云南、河南、广西、湖南、四川6个省区，国家扶贫开发工作重点县农民人均收入还不及全国平均水平的65%。习近平指出：“我国经济发展的‘蛋糕’不断做大，但分配不公问题比较突出，收入差距、城乡区域公共服务水平差距较大。在共享改革发展成果上，无论是实际情况还是制度设计，都还有不完善的地方。为此，我们必须坚持发展为了人民、发展依靠人民、发展成果由人民共享，作出更有效的制度安排，使全体人民朝着共同富裕方向稳步前进，绝不能出现‘富者累巨万，而贫者食糟糠’的现象。”</span></p>
    <p><span>　　实现全民共享，就是要把不断做大的“蛋糕”分好，让社会主义制度的优越性得到更充分体现，让人民群众有更多获得感。习近平在十八届三中全会第二次全体会议上指出：“‘蛋糕’不断做大了，同时还要把‘蛋糕’分好。我国社会历来有‘不患寡而患不均’的观念。我们要在不断发展的基础上尽量把促进社会公平正义的事情做好，既尽力而为、又量力而行，努力使全体人民在学有所教、劳有所得、病有所医、老有所养、住有所居上持续取得新进展。”习近平强调：“我们的人民热爱生活，期盼有更好的教育、更稳定的工作、更满意的收入、更可靠的社会保障、更高水平的医疗卫生服务、更舒适的居住条件、更优美的环境，期盼孩子们能成长得更好、工作得更好、生活得更好。人民对美好生活的向往，就是我们的奋斗目标。”《中共中央关于全面深化改革若干重大问题的决定》指出：“规范收入分配秩序，完善收入分配调控体制机制和政策体系，建立个人收入和财产信息系统，保护合法收入，调节过高收入，清理规范隐性收入，取缔非法收入，增加低收入者收入，扩大中等收入者比重，努力缩小城乡、区域、行业收入分配差距，逐步形成橄榄型分配格局。”</span></p>
    <p><span>　　特别是要加大对困难群众的帮扶力度，坚决打赢农村贫困人口脱贫攻坚战。习近平在2015年中央扶贫开发工作会议上强调：“脱贫攻坚战的冲锋号已经吹响，我们要立下愚公移山志，咬定目标、苦干实干，坚决打赢脱贫攻坚战，确保到2020年所有贫困地区、贫困人口一道迈入全面小康社会。”中共中央、国务院印发《关于打赢脱贫攻坚战的决定》，对打赢脱贫攻坚战作出全面系统部署。当前，脱贫攻坚已经到了啃硬骨头、攻坚拔寨的冲刺阶段，我们必须按照中央部署，实施精准扶贫、精准脱贫，以更大的决心、更明确的思路、更精准的举措、超常规的力度，众志成城实现脱贫攻坚目标，决不能落下一个贫困地区、一个贫困群众，使发展成果更多更公平惠及全体人民。</span></p>
  </blockquote>
