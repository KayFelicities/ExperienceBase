  <blockquote><span>三、共享是共建共享</span></blockquote>
  <blockquote>
    <p><span></span></p>
    <p><span>　　“享天下之利者，任天下之患；居天下之乐者，同天下之忧。”共建共享是共享实现的途径。习近平指出：“共建才能共享，共建的过程也是共享的过程。要充分发扬民主，广泛汇聚民智，最大激发民力，形成人人参与、人人尽力、人人都有成就感的生动局面。”</span></p>
    <p><span>　　“春种一粒粟，秋收万颗子。”天下没有免费的午餐，没有共建就没有共享。“共建”是“共享”的基础和前提，“共享”是“共建”的目的和方向。坚持共享发展，既追求人人享有，也要求人人参与、人人尽力，人人都为国家发展、民族振兴和个人幸福贡献自己的力量。全面小康的美好蓝图，需要共同付出艰苦的努力。众人拾柴火焰高、众人划桨开大船，共享的成果需要大家共同栽培。要在全社会营造人人参与、人人尽力、人人享有的良好环境，以共享引领共建、以共建推动共享，厚植发展优势，凝聚发展合力，提升发展境界。共享不是不劳而获，而是要共同奋斗。“大鹏之动，非一羽之轻也；骐骥之速，非一足之力也。”要调动全体人民的主观能动性，为各行业各方面的劳动者、企业家、创新人才、各级干部创造发挥作用的舞台和环境。每个人都应秉持“天下兴亡，匹夫有责”的精神，继续发扬艰苦奋斗的精神，有梦想、敢担当、重奋斗、讲奉献，为国家为民族也为自己放射生命的光和热。习近平指出，要“充分调动人民群众的积极性、主动性、创造性，举全民之力推进中国特色社会主义事业，不断把‘蛋糕’做大。”</span></p>
    <p><span>　　“用众人之力，则无不胜也。”集体是力量的源泉，众人是智慧的摇篮。马克思历史唯物史观告诉我们：人民群众是社会历史的创造者，是社会变革的决定力量。人民群众既是改革发展成果的创造者，也是改革发展成果的享有者。共同建设是全体社会成员的共同责任；共享发展成果是全体人民的应有权利。在共建“蛋糕”的过程中，要始终坚持人民群众的主体地位，尊重人民群众的首创精神，充分依靠和发挥广大人民群众的力量和智慧，调动广大人民群众的积极性，为全社会创造更多更好的物质财富和精神财富。习近平强调：“我们的责任，就是要团结带领全党全国各族人民，继续解放思想，坚持改革开放，不断解放和发展社会生产力，努力解决群众的生产生活困难，坚定不移走共同富裕的道路。”</span></p>
  </blockquote>
