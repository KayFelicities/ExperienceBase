  <blockquote>
    <p><span>二、共享是全面共享</span></p>
  </blockquote>
  <blockquote>
    <p><span>　　“千钧将一羽，轻重在平衡。”全面共享是讲共享的内容要全面。习近平指出：“共享发展就要共享国家经济、政治、文化、社会、生态各方面建设成果，全面保障人民在各方面的合法权益。”</span></p>
    <p><span>　　人们往往仅从经济角度、从物质财富分配的角度来理解“共享”，习近平对共享内涵的讲解帮助我们拓展了对“共享”的认识。“共享”不仅仅拘泥于经济领域，举凡政治、文化、社会、生态等各种领域都应包含在内。当今的中国充满活力，人民对生活品质的要求不仅限于物质财富领域，而是对社会生活的多个领域都有各种各样的诉求。在政治和社会领域共享。进一步维护公平正义，保障人民平等参与、平等发展的权利。完善社会管理制度，让人民有更多的获得感，保障人人共享发展成果，人人都有人生出彩机会。党的十八大提出“逐步建立以权利公平、机会公平、规则公平为主要内容的社会公平保障体系”，十八届四中全会进一步提出要“强化规则意识”“加快完善体现权利公平、机会公平、规则公平的法律制度”。这些都是从政治和社会领域为每一个人设置一个公平竞争的规则，让每一个人都不会输在起跑线上。习近平指出：“全面深化改革必须着眼创造更加公平正义的社会环境，不断克服各种有违公平正义的现象，使改革发展成果更多更公平惠及全体人民。”习近平强调：“对由于制度安排不健全造成的有违公平正义的问题要抓紧解决，使我们的制度安排更好体现社会主义公平正义原则，更加有利于实现好、维护好、发展好最广大人民根本利益。”在文化领域共享。通过文化、教育制度的建设，实现文化、教育服务均衡，提升国民素质，提高社会文明程度，让人民在文化领域实现共享。习近平指出：“满足人民日益增长的精神文化需求，必须抓好文化建设，增加社会的精神文化财富。”从这个角度出发，国家致力于提高教育质量，推动义务教育均衡发展，同时要求教育资源更多地向贫困地区和少数民族地区倾斜。2015年2月14日，在陕西延安考察时习近平指出：“教育很重要，革命老区、贫困地区要脱贫致富，从根儿上还是要把教育抓好，不能让孩子输在起跑线上。国家的资金会向教育倾斜、向基础教育倾斜、向革命老区基础教育倾斜。”全面共享必须包括人民随着生活质量的提升而不断变化的精神文化生活的共享。习近平指出：“文学、戏剧、电影、电视、音乐、舞蹈、美术、摄影、书法、曲艺、杂技以及民间文艺、群众文艺等各领域都要跟上时代发展、把握人民需求，以充沛的激情、生动的笔触、优美的旋律、感人的形象创作生产出人民喜闻乐见的优秀作品，让人民精神文化生活不断迈上新台阶。”“十三五”规划纲要提出：“推进文化事业和文化产业双轮驱动，实施重大文化工程和文化名家工程，为全体人民提供昂扬向上、多姿多彩、怡养情怀的精神食粮。”在生态领域共享。加快生态文明建设步伐，还人民以青山绿水。要认识到环境就是民生，青山就是美丽，蓝天也是幸福，绿水青山就是金山银山，保护环境，改善环境，让人民共享美好的生活环境。习近平指出：“在30多年持续快速发展中，我国农产品、工业品、服务产品的生产能力迅速扩大，但提供优质生态产品的能力却在减弱，一些地方生态环境还在恶化。这就要求我们尽力补上生态文明建设这块短板，切实把生态文明的理念、原则、目标融入经济社会发展各方面，贯彻落实到各级各类规划和各项工作中。”习近平强调：“要树立大局观、长远观、整体观，坚持节约资源和保护环境的基本国策，像保护眼睛一样保护生态环境，像对待生命一样对待生态环境，推动形成绿色发展方式和生活方式，协同推进人民富裕、国家强盛、中国美丽。”“十三五”规划纲要提出：“以提高环境质量为核心，以解决生态环境领域突出问题为重点，加大生态环境保护力度，提高资源利用效率，为人民提供更多优质生态产品，协同推进人民富裕、国家富强、中国美丽。”</span></p>
  </blockquote>
