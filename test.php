  <blockquote>
    <p><span>　　唯有深刻理解共享发展理念的内涵，才能更好落实共享发展理念；唯有坚持全民共享、全面共享、共建共享、渐进共享，不断做大“蛋糕”、分好“蛋糕”，才能使发展成果更多更公平惠及全体人民。习近平指出：“这4方面是相互贯通的，要整体理解和把握。”“落实共享发展是一门大学问，要做好从顶层设计到‘最后一公里’落地的工作，在实践中不断取得新成效。”</span><span></span></p>
  </blockquote>
